# Output folder
os.makedirs("scam_dataset_hindi", exist_ok=True)

for i, phrase in enumerate(scam_phrases, start=1):
    tts = gTTS(text=phrase, lang="hi")
    mp3_path = f"scam_dataset_hindi/scam_{i:02}.mp3"
    wav_path = f"scam_dataset_hindi/scam_{i:02}.wav"
    
    # Save as MP3
    tts.save(mp3_path)
    
    # Convert to WAV
    AudioSegment.from_mp3(mp3_path).export(wav_path, format="wav")

